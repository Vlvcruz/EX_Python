#Escreva um programa que peça ao usuário um número e imprima se é positivo ou negativo.

#entrada de dados

nmr = float(input("Digite um numero: "))

#Condicional para saber valor do numero
if nmr > 0:
    print("Seu numero e positivo !")#Saida de dados
elif nmr == 0:
    print("Numero Nulo !")
else:
    print("Seu numero e Negative !")#Saida de dados