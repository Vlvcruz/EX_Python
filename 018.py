#Escreva um programa que peça ao usuário uma idade e imprima se é maior ou menor de idade (18 anos).

#entrada de dados
idade = float(input("Digite uma idade: "))

#Condicional para saber se é maior de idade
if idade > 17:
    print("Maior de idade !")#Saida de dados
else:
    print("Menor de idade !")#Saida de dados